function placeMines(safeR,safeC){
  let placed=0;
  while(placed<mineMines){
    const r=Math.floor(Math.random()*mineRows);
    const c=Math.floor(Math.random()*mineCols);
    if(mineGrid[r][c]!=='M'&&!(Math.abs(r-safeR)<=1&&Math.abs(c-safeC)<=1)){
      mineGrid[r][c]='M'; placed++;
    }
  }
  for(let r=0;r<mineRows;r++) for(let c=0;c<mineCols;c++){
    if(mineGrid[r][c]==='M') continue;
    let cnt=0;
    for(let dr=-1;dr<=1;dr++) for(let dc=-1;dc<=1;dc++){
      const nr=r+dr,nc=c+dc;
      if(nr>=0&&nr<mineRows&&nc>=0&&nc<mineCols&&mineGrid[nr][nc]==='M') cnt++;
    }
    mineGrid[r][c]=cnt;
  }
}

