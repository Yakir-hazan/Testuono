function updateLastAlertBadge() {
  try {
    const timeEl = document.getElementById('lastAlertTime');
    if (!timeEl) return;

    // נסה לקרוא מ-alert_status.json (מעודכן כל דקה ע"י GitHub Actions)
    fetch('/alert_status.json?t=' + Date.now(), { cache: 'no-store' })
      .then(r => r.json())
      .then(status => {
        const ts = status.timestamp || 0;
        const cities = status.cities || [];
        if (!ts) { timeEl.textContent = 'אזעקה אחרונה: אין מידע'; return; }

        const diff = Math.floor((Date.now()/1000) - ts);
        let timeStr = '';
        if (diff < 60) timeStr = `לפני ${diff} שניות`;
        else if (diff < 3600) timeStr = `לפני ${Math.floor(diff/60)} דקות`;
        else if (diff < 86400) timeStr = `לפני ${Math.floor(diff/3600)} שעות`;
        else {
          const d = new Date(ts * 1000);
          timeStr = d.toLocaleDateString('he-IL', { day:'numeric', month:'numeric' }) + ' ' +
                    d.toLocaleTimeString('he-IL', { hour:'2-digit', minute:'2-digit' });
        }
        const parts = cities.slice(0, 2).join(' • ') + (cities.length > 2 ? ` +${cities.length-2}` : '');
        timeEl.textContent = timeStr + (parts ? ` (${parts})` : '');
      })
      .catch(() => {
        // fallback ל-localStorage
        const saved = localStorage.getItem('mamad_last_alert');
        if (!saved) { timeEl.textContent = 'אזעקה אחרונה: אין מידע'; return; }
        const { areas, timestamp } = JSON.parse(saved);
        const diff = Math.floor((Date.now() - timestamp) / 1000);
        let timeStr = diff < 60 ? `לפני ${diff} שניות` :
                      diff < 3600 ? `לפני ${Math.floor(diff/60)} דקות` :
                      `לפני ${Math.floor(diff/3600)} שעות`;
        timeEl.textContent = timeStr + (areas ? ` (${areas})` : '');
      });
  } catch(e) {}
}

