function solitaireDraw(){
  if(!solStock.length){
    solStock=[...solWaste].reverse().map(c=>({...c,up:false}));
    solWaste=[];
  } else {
    const c=solStock.pop(); c.up=true; solWaste.push(c);
  }
  solSel=null; playClick(); solDraw();
}

// layout — הכל מחושב ממימד אחד
function solLayout(){
  const cv=document.getElementById('solitaireCanvas');
  const W=cv.width;
  const PAD=3;
  const CW=Math.floor((W-PAD*8)/7);
  const CH=Math.round(CW*1.45);
  // x של כל עמודה
  const colX=i=>PAD+i*(CW+PAD);
  return {W,CW,CH,PAD,colX};
}

function solHandleClick(x,y,dbl){
  const {CW,CH,PAD,colX}=solLayout();
  const TOP=CH+PAD*2; // גובה שורת עליונה

  // ── STOCK ──
  const sx=colX(0);
  if(x>=sx&&x<=sx+CW&&y>=PAD&&y<=PAD+CH){
    solitaireDraw(); return;
  }

  // ── WASTE ──
  const wx=colX(1);
  if(x>=wx&&x<=wx+CW&&y>=PAD&&y<=PAD+CH){
    if(!solWaste.length){solDraw();return;}
    const card=solWaste[solWaste.length-1];
    if(dbl){solAutoFoundation(card,true,0,0);solSel=null;solDraw();return;}
    solSel=solSel&&solSel.from==='waste'?null:{from:'waste',card};
    solDraw(); return;
  }

  // ── FOUNDATIONS (עמודות 3-6) ──
  for(let f=0;f<4;f++){
    const fx=colX(3+f);
    if(x>=fx&&x<=fx+CW&&y>=PAD&&y<=PAD+CH){
      if(solSel) solTryFoundation(f);
      solSel=null; solDraw(); return;
    }
  }

  // ── TABLEAU ──
  const cv=document.getElementById('solitaireCanvas');
  for(let col=0;col<7;col++){
    const cx=colX(col);
    if(x<cx||x>cx+CW) continue;
    const stack=solTableau[col];

    // עמודה ריקה
    if(!stack.length){
      if(y>=TOP&&y<=TOP+CH&&solSel){solTryTableau(col);solSel=null;}
      solDraw(); return;
    }

    const step=solColStep(col,TOP,CH,cv.height);

    for(let row=stack.length-1;row>=0;row--){
      const cy=TOP+row*step;
      const bottom=row===stack.length-1?cy+CH:cy+step;
      if(y<cy||y>bottom) continue;
      const card=stack[row];

      // קלף הפוך — הפוך אם אחרון
      if(!card.up){
        if(row===stack.length-1){card.up=true;playClick();solDraw();}
        return;
      }

      // טפיחה כפולה → שלח לבסיס
      if(dbl&&row===stack.length-1){
        solAutoFoundation(card,false,col,row);
        solSel=null; solDraw(); return;
      }

      if(solSel){
        solTryTableau(col,row);
      } else {
        solSel={from:'tableau',col,row,card};
      }
      solDraw(); return;
    }
  }

  solSel=null; solDraw();
}




function solHint(){
  // מצא מהלך חוקי ראשון — בזבל → בסיס, בזבל → tableau, tableau → בסיס, tableau → tableau
  const tryMoves=[
    // waste → foundation
    ()=>{
      if(!solWaste.length) return null;
      const card=solWaste[solWaste.length-1];
      for(let f=0;f<4;f++){
        const found=solFoundations[f];
        const top=found.cards[found.cards.length-1];
        if(card.s===found.s&&((!top&&card.n===1)||(top&&card.n===top.n+1))) return {msg:'משוך קלף מהפסולת אל הבסיס!'};
      }
      return null;
    },
    // waste → tableau
    ()=>{
      if(!solWaste.length) return null;
      const card=solWaste[solWaste.length-1];
      for(let col=0;col<7;col++){
        const stack=solTableau[col];
        const top=stack[stack.length-1];
        if((!top&&card.n===13)||(top&&top.up&&top.n===card.n+1&&top.red!==card.red))
          return {msg:`העבר קלף מהפסולת לעמודה ${col+1}`};
      }
      return null;
    },
    // tableau → foundation
    ()=>{
      for(let col=0;col<7;col++){
        const stack=solTableau[col];
        if(!stack.length) continue;
        const card=stack[stack.length-1];
        if(!card.up) continue;
        for(let f=0;f<4;f++){
          const found=solFoundations[f];
          const top=found.cards[found.cards.length-1];
          if(card.s===found.s&&((!top&&card.n===1)||(top&&card.n===top.n+1)))
            return {msg:`שלח קלף מעמודה ${col+1} לבסיס!`};
        }
      }
      return null;
    },
    // tableau → tableau
    ()=>{
      for(let fromCol=0;fromCol<7;fromCol++){
        const stack=solTableau[fromCol];
        for(let row=0;row<stack.length;row++){
          const card=stack[row];
          if(!card.up) continue;
          for(let toCol=0;toCol<7;toCol++){
            if(toCol===fromCol) continue;
            const dest=solTableau[toCol];
            const top=dest[dest.length-1];
            if((!top&&card.n===13)||(top&&top.up&&top.n===card.n+1&&top.red!==card.red))
              return {msg:`העבר קלף מעמודה ${fromCol+1} לעמודה ${toCol+1}`};
          }
        }
      }
      return null;
    },
    // stock exists
    ()=>{ if(solStock.length) return {msg:'משוך קלף חדש מהחפיסה'}; return null; },
  ];
  for(const fn of tryMoves){
    const res=fn();
    if(res){showToast('💡 '+res.msg,3000);return;}
  }
  showToast('💡 אין מהלכים — נסה למשוך מהחפיסה',2500);
}

function solAutoComplete(){
  // בדוק שכל הקלפים בטאבלו גלויים
  const allUp=solTableau.every(col=>col.every(c=>c.up));
  if(!allUp){showToast('⚠️ לא ניתן — יש קלפים הפוכים',2000);return;}
  // השלם אוטומטית
  let moved=true;
  let steps=0;
  while(moved&&steps<200){
    moved=false; steps++;
    // מ-waste
    if(solWaste.length){
      if(solAutoFoundation(solWaste[solWaste.length-1],true,0,0)){moved=true;continue;}
    }
    // מ-tableau
    for(let col=0;col<7;col++){
      const stack=solTableau[col];
      if(!stack.length) continue;
      const card=stack[stack.length-1];
      if(solAutoFoundation(card,false,col,stack.length-1)){moved=true;break;}
    }
  }
  solDraw();
  if(solFoundations.every(f=>f.cards.length===13)){
    document.getElementById('solitaireMsg').textContent='🎉 ניצחת/ת!'; playWin();
  } else {
    showToast('השלמה חלקית — המשך בעצמך!',2000);
  }
}

function solColStep(col,TOP,CH,canvasH){
  const stack=solTableau[col];
  if(stack.length<=1) return 0;
  const avail=canvasH-TOP-CH-8;
  return Math.min(32,Math.max(16,Math.floor(avail/(stack.length-1))));
}

function solAutoFoundation(card,fromWaste,col,row){
  for(let f=0;f<4;f++){
    const found=solFoundations[f];
    const top=found.cards[found.cards.length-1];
    if(card.s!==found.s) continue;
    if((!top&&card.n===1)||(top&&card.n===top.n+1)){
      found.cards.push(card);
      if(fromWaste) solWaste.pop();
      else {
        solTableau[col].splice(row);
        const src=solTableau[col];
        if(src.length&&!src[src.length-1].up) src[src.length-1].up=true;
      }
      playSuccess();
      if(solFoundations.every(f=>f.cards.length===13)){
        document.getElementById('solitaireMsg').textContent='🎉 ניצחת/ת!'; playWin();
      }
      return true;
    }
  }
  return false;
}

function solTryFoundation(f){
  if(!solSel) return;
  const card=solSel.card;
  if(solSel.from==='tableau'&&solSel.row!==solTableau[solSel.col].length-1){solSel=null;return;}
  const found=solFoundations[f];
  const top=found.cards[found.cards.length-1];
  if(card.s===found.s&&((!top&&card.n===1)||(top&&card.n===top.n+1))){
    found.cards.push(card);
    solRemoveSel();
    playSuccess();
    if(solFoundations.every(f=>f.cards.length===13)){
      document.getElementById('solitaireMsg').textContent='🎉 ניצחת/ת!'; playWin();
    }
  } else playError();
  solSel=null;
}

function solTryTableau(toCol){
  if(!solSel) return;
  const stack=solTableau[toCol];
  const top=stack[stack.length-1];
  let moving=[];
  if(solSel.from==='waste') moving=[solSel.card];
  else moving=solTableau[solSel.col].slice(solSel.row);
  if(!moving.length){solSel=null;return;}
  const bottom=moving[0];
  const ok=(!top&&bottom.n===13)||(top&&top.up&&top.n===bottom.n+1&&top.red!==bottom.red);
  if(ok){
    if(solSel.from==='waste'){
      solWaste.pop(); stack.push(bottom);
    } else {
      if(solSel.col===toCol){solSel=null;return;}
      const src=solTableau[solSel.col];
      stack.push(...src.splice(solSel.row));
      if(src.length&&!src[src.length-1].up) src[src.length-1].up=true;
    }
    playSuccess();
  } else playError();
  solSel=null;
}

function solRemoveSel(){
  if(!solSel) return;
  if(solSel.from==='waste') solWaste.pop();
  else {
    const src=solTableau[solSel.col];
    src.splice(solSel.row);
    if(src.length&&!src[src.length-1].up) src[src.length-1].up=true;
  }
}

function solDraw(){
  const cv=document.getElementById('solitaireCanvas');
  const W=cv.parentElement.offsetWidth||320;
  cv.width=W;
  const {CW,CH,PAD,colX}=solLayout();
  const TOP=CH+PAD*2;
  const maxLen=Math.max(...solTableau.map(s=>s.length),1);
  const estStep=Math.min(32,Math.max(16,Math.floor((W*1.4)/maxLen)));
  cv.height=TOP+maxLen*estStep+CH+20;

  const ctx=cv.getContext('2d');
  ctx.fillStyle='#0d1521';
  ctx.fillRect(0,0,cv.width,cv.height);

  function rr(x,y,w,h,r){
    ctx.beginPath();
    ctx.moveTo(x+r,y); ctx.lineTo(x+w-r,y); ctx.arcTo(x+w,y,x+w,y+r,r);
    ctx.lineTo(x+w,y+h-r); ctx.arcTo(x+w,y+h,x+w-r,y+h,r);
    ctx.lineTo(x+r,y+h); ctx.arcTo(x,y+h,x,y+h-r,r);
    ctx.lineTo(x,y+r); ctx.arcTo(x,y,x+r,y,r);
    ctx.closePath();
  }

  function drawCard(x,y,card,sel){
    const r=4;
    ctx.shadowColor='rgba(0,0,0,0.5)'; ctx.shadowBlur=5;
    ctx.fillStyle=sel?'rgba(77,142,255,0.35)':'#1e2d45';
    rr(x,y,CW,CH,r); ctx.fill(); ctx.shadowBlur=0;
    ctx.strokeStyle=sel?'#4d8eff':'rgba(255,255,255,0.12)';
    ctx.lineWidth=sel?2:1; rr(x,y,CW,CH,r); ctx.stroke();

    if(!card){ // חריץ ריק
      ctx.strokeStyle='rgba(255,255,255,0.07)'; ctx.lineWidth=1.5; ctx.setLineDash([4,4]);
      rr(x+2,y+2,CW-4,CH-4,r); ctx.stroke(); ctx.setLineDash([]); return;
    }
    if(!card.up){ // גב הקלף
      const g=ctx.createLinearGradient(x,y,x+CW,y+CH);
      g.addColorStop(0,'#1a3a6a'); g.addColorStop(1,'#0d1f3c');
      ctx.fillStyle=g; rr(x+2,y+2,CW-4,CH-4,r); ctx.fill();
      ctx.fillStyle='rgba(77,142,255,0.12)';
      for(let px=x+6;px<x+CW-3;px+=6) for(let py=y+6;py<y+CH-3;py+=6){
        ctx.beginPath(); ctx.arc(px,py,1,0,Math.PI*2); ctx.fill();
      }
      return;
    }
    // פנים
    ctx.fillStyle='#f0f4ff'; rr(x+1,y+1,CW-2,CH-2,r); ctx.fill();
    const clr=card.red?'#d01840':'#0a1628';
    const fs=Math.max(9,Math.floor(CW/3.5));
    ctx.fillStyle=clr; ctx.textAlign='left';
    ctx.font=`bold ${fs}px sans-serif`; ctx.fillText(card.v,x+3,y+fs+2);
    ctx.font=`${Math.max(7,fs-2)}px sans-serif`; ctx.fillText(card.s,x+3,y+fs*2+2);
    ctx.font=`bold ${Math.floor(CW/2.2)}px sans-serif`; ctx.textAlign='center';
    ctx.fillText(card.s,x+CW/2,y+CH/2+Math.floor(CW/4.5));
  }

  // STOCK
  const sx=colX(0);
  if(solStock.length) drawCard(sx,PAD,{up:false},false);
  else {
    ctx.strokeStyle='rgba(255,255,255,0.1)'; ctx.lineWidth=1.5; ctx.setLineDash([4,4]);
    rr(sx,PAD,CW,CH,4); ctx.stroke(); ctx.setLineDash([]);
    ctx.fillStyle='rgba(255,255,255,0.35)'; ctx.font=`${Math.floor(CW*0.5)}px sans-serif`; ctx.textAlign='center';
    ctx.fillText('↺',sx+CW/2,PAD+CH/2+8);
  }

  // WASTE
  const wx=colX(1);
  if(solWaste.length){
    drawCard(wx,PAD,solWaste[solWaste.length-1],solSel&&solSel.from==='waste');
  } else {
    ctx.strokeStyle='rgba(255,255,255,0.05)'; ctx.lineWidth=1; ctx.setLineDash([3,3]);
    rr(wx,PAD,CW,CH,4); ctx.stroke(); ctx.setLineDash([]);
  }

  // FOUNDATIONS
  for(let f=0;f<4;f++){
    const fx=colX(3+f);
    const top=solFoundations[f].cards[solFoundations[f].cards.length-1];
    if(top) drawCard(fx,PAD,top,false);
    else {
      drawCard(fx,PAD,null,false);
      ctx.fillStyle='rgba(255,255,255,0.18)'; ctx.font=`${Math.floor(CW/2)}px sans-serif`; ctx.textAlign='center';
      ctx.fillText(SOL_SUITS[f],fx+CW/2,PAD+CH/2+8);
    }
  }

  // TABLEAU
  for(let col=0;col<7;col++){
    const cx=colX(col);
    const stack=solTableau[col];
    if(!stack.length){
      ctx.strokeStyle='rgba(255,255,255,0.06)'; ctx.lineWidth=1; ctx.setLineDash([3,3]);
      rr(cx,TOP,CW,CH,4); ctx.stroke(); ctx.setLineDash([]); continue;
    }
    const step=solColStep(col,TOP,CH,cv.height);
    stack.forEach((card,row)=>{
      const sel=solSel&&solSel.from==='tableau'&&solSel.col===col&&solSel.row<=row;
      drawCard(cx,TOP+row*step,card,sel);
    });
  }
}