let canvas, ctx2d, drawing = false, color = '#5b8dee', brushSize = 6, erasing = false;
const colors = ['#5b8dee','#7c6be8','#f06292','#ff9800','#34c77b','#26c6da','#ff5252','#2a2a3a','#fff'];
const sizes = [3, 6, 12, 20];

function initCanvas() {
  canvas = document.getElementById('drawCanvas');
  ctx2d = canvas.getContext('2d');
  canvas.width = canvas.offsetWidth;
  canvas.height = 300;
  ctx2d.fillStyle = '#fff'; ctx2d.fillRect(0,0,canvas.width,canvas.height);

  const palette = document.getElementById('colorPalette');
  palette.innerHTML = '';
  colors.forEach((c, i) => {
    const d = document.createElement('div');
    d.className = 'color-dot' + (i === 0 ? ' active' : '');
    d.style.background = c;
    if (c === '#fff') d.style.border = '3px solid #ccc';
    d.onclick = () => { color = c; erasing = false;
      document.querySelectorAll('.color-dot').forEach(x => x.classList.remove('active'));
      d.classList.add('active');
    };
    palette.appendChild(d);
  });

  const bsizes = document.getElementById('brushSizes');
  bsizes.innerHTML = '';
  sizes.forEach((s, i) => {
    const d = document.createElement('div');
    d.className = 'brush-size' + (i === 1 ? ' active' : '');
    d.style.width = d.style.height = s + 'px';
    d.onclick = () => { brushSize = s;
      document.querySelectorAll('.brush-size').forEach(x => x.classList.remove('active'));
      d.classList.add('active');
    };
    bsizes.appendChild(d);
  });

  const getPos = (e) => {
    const rect = canvas.getBoundingClientRect();
    const src = e.touches ? e.touches[0] : e;
    return { x: src.clientX - rect.left, y: src.clientY - rect.top };
  };
  canvas.onmousedown = canvas.ontouchstart = (e) => { e.preventDefault(); drawing = true; const p = getPos(e); ctx2d.beginPath(); ctx2d.moveTo(p.x, p.y); };
  canvas.onmousemove = canvas.ontouchmove = (e) => { e.preventDefault(); if (!drawing) return; const p = getPos(e); ctx2d.strokeStyle = erasing ? '#fff' : color; ctx2d.lineWidth = erasing ? brushSize * 3 : brushSize; ctx2d.lineCap = 'round'; ctx2d.lineJoin = 'round'; ctx2d.lineTo(p.x, p.y); ctx2d.stroke(); };
  canvas.onmouseup = canvas.ontouchend = () => drawing = false;
}
function clearCanvas() { if(ctx2d) { ctx2d.fillStyle='#fff'; ctx2d.fillRect(0,0,canvas.width,canvas.height); } }
