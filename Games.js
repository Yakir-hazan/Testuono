function pongLoop() {
  if(!pongRunning){return;}
  const s=pongState;
  // AI paddle
  s.p2y+=(s.by-s.p2y)*0.07;
  s.p2y=Math.max(s.ph/2,Math.min(s.H-s.ph/2,s.p2y));
  // move ball
  s.bx+=s.bdx; s.by+=s.bdy;
  if(s.by<s.br||s.by>s.H-s.br){s.bdy*=-1;playClick();}
  // right paddle (player)
  if(s.bx>s.W-s.pw-s.br&&Math.abs(s.by-s.p1y)<s.ph/2){s.bdx=-(Math.abs(s.bdx)+0.2);s.bdy+=(s.by-s.p1y)*0.05;playClick();}
  // left paddle (AI)
  if(s.bx<s.pw+s.br&&Math.abs(s.by-s.p2y)<s.ph/2){s.bdx=Math.abs(s.bdx)+0.1;playClick();}
  // score
  if(s.bx<0){s.s1++;s.bx=s.W/2;s.by=s.H/2;s.bdx=3;s.bdy=2.5;playError();document.getElementById('pongScore').textContent=s.s2+' : '+s.s1;}
  if(s.bx>s.W){s.s2++;s.bx=s.W/2;s.by=s.H/2;s.bdx=-3;s.bdy=2.5;playSuccess();document.getElementById('pongScore').textContent=s.s2+' : '+s.s1;}
  drawPong();
  pongRAF=requestAnimationFrame(pongLoop);
}
