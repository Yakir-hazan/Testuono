let tttBoard, tttCurrent, tttOver;
function initTTT() {
  tttBoard = Array(9).fill(null); tttCurrent = 'X'; tttOver = false;
  document.getElementById('tttStatus').textContent = 'תור של: ❌';
  const board = document.getElementById('tttBoard');
  board.innerHTML = '';
  for (let i = 0; i < 9; i++) {
    const cell = document.createElement('div');
    cell.className = 'ttt-cell';
    cell.onclick = () => tttMove(i, cell);
    board.appendChild(cell);
  }
}
function tttMove(i, cell) {
  if (tttBoard[i] || tttOver) return;
  playClick();
  tttBoard[i] = tttCurrent;
  cell.textContent = tttCurrent === 'X' ? '❌' : '⭕';
  cell.classList.add('taken');
  const winner = checkTTT();
  if (winner) {
    tttOver = true;
    playWin();
    document.getElementById('tttStatus').textContent = `🎉 ${winner === 'X' ? '❌' : '⭕'} ניצח!`;
  } else if (!tttBoard.includes(null)) {
    tttOver = true;
    playError();
    document.getElementById('tttStatus').textContent = '🤝 תיקו!';
  } else {
    tttCurrent = tttCurrent === 'X' ? 'O' : 'X';
    document.getElementById('tttStatus').textContent = `תור של: ${tttCurrent === 'X' ? '❌' : '⭕'}`;
  }
}
function checkTTT() {
  const wins = [[0,1,2],[3,4,5],[6,7,8],[0,3,6],[1,4,7],[2,5,8],[0,4,8],[2,4,6]];
  for (const [a,b,c] of wins) if (tttBoard[a] && tttBoard[a] === tttBoard[b] && tttBoard[a] === tttBoard[c]) return tttBoard[a];
  return null;
}

