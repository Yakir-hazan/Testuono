function getLiveUsers() {
  const now = new Date();
  const hour = now.getHours();
  const min = now.getMinutes();
  const sec = now.getSeconds();
  // בסיס לפי שעה — בין 0 ל-2500
  let base;
  if (hour >= 2 && hour < 6) base = 80;
  else if (hour >= 6 && hour < 8) base = 600;
  else if (hour >= 8 && hour < 12) base = 1400;
  else if (hour >= 12 && hour < 14) base = 1800;
  else if (hour >= 14 && hour < 18) base = 1600;
  else if (hour >= 18 && hour < 22) base = 2100;
  else if (hour >= 22) base = 900;
  else base = 400;
  // תנודות טבעיות
  const wave = Math.sin((min * 60 + sec) / 180) * 100;
  const noise = Math.sin(sec * 1.7) * 40 + Math.cos(sec * 0.9) * 30;
  return Math.max(0, Math.min(2500, Math.round(base + wave + noise)));
}

