function boResetBall() {
  const w = boCanvas ? boCanvas.width : 300;
  const h = boCanvas ? boCanvas.height : 300;
  const spd = 3 + boLevel * 0.4;
  boPaddle = { x: w/2-45, y: h-24, w:90, h:13 };
  boBall = { x:w/2, y:h-50, vx:spd*(Math.random()>0.5?1:-1), vy:-spd, r:8 };
}
function boBuildLevel() {
  const w = boCanvas.width;
  boBricks = [];
  const cols = 6 + Math.min(boLevel-1,2);
  const rows = 4 + Math.min(boLevel-1,3);
  const bw = (w - 20) / cols - 4;
  const colors = ['#e94560','#f59563','#fed330','#26de81','#4d8eff','#a55eea','#00d4aa'];
  for(let r=0;r<rows;r++) for(let c=0;c<cols;c++) {
    const hp = r<2 ? 2 : 1;
    boBricks.push({x:10+c*(bw+4), y:50+r*22, w:bw, h:15, alive:true, color:colors[(r+boLevel-1)%colors.length], hp, maxHp:hp});
  }
}
function boUpdateHUD() {
  const el = document.getElementById('scoreBreakout');
  if(el) el.textContent = `${boScore} | ❤️${boLives} | Lv${boLevel}`;
}
function stopBreakout() { boRunning=false; cancelAnimationFrame(boAnim); }
function loopBreakout() {
  if(!boRunning) return;
  const w=boCanvas.width, h=boCanvas.height;
  // bg gradient
  const grad=boCtx.createLinearGradient(0,0,0,h);
  grad.addColorStop(0,'#0d0f1f'); grad.addColorStop(1,'#1a1a2e');
  boCtx.fillStyle=grad; boCtx.fillRect(0,0,w,h);

  // waiting overlay
  if(boWaiting){
    boCtx.fillStyle='rgba(0,0,0,0.55)'; boCtx.fillRect(0,0,w,h);
    boCtx.fillStyle='#fff'; boCtx.font=`bold ${Math.round(w*0.055)}px Arial`; boCtx.textAlign='center';
    boCtx.fillText('לחץ/י להמשיך',w/2,h/2);
    boAnim=requestAnimationFrame(loopBreakout); return;
  }

  // paddle glow
  boCtx.shadowColor='#4d8eff'; boCtx.shadowBlur=16;
  boCtx.fillStyle='#4d8eff';
  boCtx.beginPath(); boCtx.roundRect(boPaddle.x,boPaddle.y,boPaddle.w,boPaddle.h,7); boCtx.fill();
  boCtx.shadowBlur=0;

  // move ball
  boBall.x+=boBall.vx; boBall.y+=boBall.vy;
  // clamp vx speed
  const spd=Math.sqrt(boBall.vx*boBall.vx+boBall.vy*boBall.vy);
  const maxSpd=10;
  if(spd>maxSpd){boBall.vx=boBall.vx/spd*maxSpd;boBall.vy=boBall.vy/spd*maxSpd;}

  if(boBall.x<boBall.r){boBall.x=boBall.r;boBall.vx=Math.abs(boBall.vx);}
  if(boBall.x>w-boBall.r){boBall.x=w-boBall.r;boBall.vx=-Math.abs(boBall.vx);}
  if(boBall.y<boBall.r){boBall.y=boBall.r;boBall.vy=Math.abs(boBall.vy);}

  // lost ball
  if(boBall.y>h+20){
    boLives--;
    boUpdateHUD();
    if(boLives<=0){
      boRunning=false;
      boCtx.fillStyle='rgba(0,0,0,0.75)'; boCtx.fillRect(0,0,w,h);
      boCtx.fillStyle='#ff5f5f'; boCtx.font=`bold ${Math.round(w*0.065)}px Arial`; boCtx.textAlign='center';
      boCtx.fillText('💔 Game Over',w/2,h/2-20);
      boCtx.fillStyle='#fff'; boCtx.font=`${Math.round(w*0.05)}px Arial`;
      boCtx.fillText(`ניקוד: ${boScore}`,w/2,h/2+28);
      boCtx.font=`${Math.round(w*0.04)}px Arial`; boCtx.fillStyle='rgba(255,255,255,0.5)';
      boCtx.fillText('לחץ 🔄 למשחק חדש',w/2,h/2+60);
      return;
    }
    boResetBall();
    boWaiting=true;
    boAnim=requestAnimationFrame(loopBreakout);
    return;
  }

  // paddle collision
  if(boBall.vy>0&&boBall.y+boBall.r>boPaddle.y&&boBall.y-boBall.r<boPaddle.y+boPaddle.h&&boBall.x>boPaddle.x-boBall.r&&boBall.x<boPaddle.x+boPaddle.w+boBall.r){
    boBall.vy=-Math.abs(boBall.vy);
    const hit=(boBall.x-(boPaddle.x+boPaddle.w/2))/(boPaddle.w/2);
    boBall.vx=hit*5; playClick();
  }

  // bricks
  boBricks.forEach(br => {
    if(!br.alive) return;
    const alpha=br.hp/br.maxHp;
    boCtx.globalAlpha=0.3+alpha*0.7;
    boCtx.shadowColor=br.color; boCtx.shadowBlur=8;
    boCtx.fillStyle=br.color;
    boCtx.beginPath(); boCtx.roundRect(br.x,br.y,br.w,br.h,4); boCtx.fill();
    boCtx.shadowBlur=0; boCtx.globalAlpha=1;
    // crack lines for hp=2
    if(br.hp<br.maxHp&&br.maxHp>1){
      boCtx.strokeStyle='rgba(0,0,0,0.4)'; boCtx.lineWidth=2;
      boCtx.beginPath(); boCtx.moveTo(br.x+br.w*0.3,br.y); boCtx.lineTo(br.x+br.w*0.5,br.y+br.h); boCtx.stroke();
    }
    if(boBall.x+boBall.r>br.x&&boBall.x-boBall.r<br.x+br.w&&boBall.y+boBall.r>br.y&&boBall.y-boBall.r<br.y+br.h){
      br.hp--;
      if(br.hp<=0){br.alive=false;boScore+=20;}else{boScore+=5;}
      document.getElementById('scoreBreakout').textContent=`${boScore} | ❤️${boLives} | Lv${boLevel}`;
      const overlapLeft=boBall.x+boBall.r-br.x, overlapRight=br.x+br.w-boBall.x+boBall.r;
      const overlapTop=boBall.y+boBall.r-br.y, overlapBottom=br.y+br.h-boBall.y+boBall.r;
      const minH=Math.min(overlapLeft,overlapRight), minV=Math.min(overlapTop,overlapBottom);
      if(minH<minV) boBall.vx*=-1; else boBall.vy*=-1;
      playClick();
    }
  });

  // ball glow
  boCtx.shadowColor='#fff'; boCtx.shadowBlur=12;
  boCtx.fillStyle='#fff'; boCtx.beginPath(); boCtx.arc(boBall.x,boBall.y,boBall.r,0,Math.PI*2); boCtx.fill();
  boCtx.shadowBlur=0;

  // level complete
  if(!boBricks.some(b=>b.alive)){
    boLevel++; boLives=Math.min(boLives+1,5);
    boUpdateHUD();
    boResetBall(); boBuildLevel();
    boWaiting=true;
    boCtx.fillStyle='rgba(0,0,0,0.6)'; boCtx.fillRect(0,0,w,h);
    boCtx.fillStyle='#fed330'; boCtx.font=`bold ${Math.round(w*0.065)}px Arial`; boCtx.textAlign='center';
    boCtx.fillText(`🎉 שלב ${boLevel-1} הושלם!`,w/2,h/2-10);
    boCtx.fillStyle='#fff'; boCtx.font=`${Math.round(w*0.042)}px Arial`;
    boCtx.fillText('לחץ/י להמשך',w/2,h/2+30);
    boAnim=requestAnimationFrame(loopBreakout); return;
  }
  boAnim = requestAnimationFrame(loopBreakout);
}

