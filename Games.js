function initCityList() {
  renderCities(ALL_CITIES);
  // טען עיר שמורה
  const saved = localStorage.getItem('mamad_city');
  if (saved) { selectedCity = saved; updateLocationBtn(); }
}

function renderCities(list) {
  const cont = document.getElementById('cityList');
  cont.innerHTML = '';
  list.forEach(city => {
    const btn = document.createElement('button');
    btn.textContent = city;
    const isSelected = city === selectedCity;
    btn.style.cssText = `
      width:100%; padding:13px 18px; border-radius:14px;
      background:${isSelected ? 'rgba(77,142,255,0.15)' : 'var(--card)'};
      border:1.5px solid ${isSelected ? 'rgba(77,142,255,0.5)' : 'var(--border)'};
      font-family:inherit; font-size:15px; font-weight:600;
      color:${isSelected ? '#4d8eff' : 'var(--text)'};
      cursor:pointer; text-align:right;
    `;
    btn.onclick = () => selectCity(city);
    cont.appendChild(btn);
  });
}

function filterCities() {
  const q = document.getElementById('citySearch').value.trim();
  const filtered = q ? ALL_CITIES.filter(c => c.includes(q)) : ALL_CITIES;
  renderCities(filtered);
}

function selectCity(city) {
  selectedCity = city;
  localStorage.setItem('mamad_city', city);
  localStorage.setItem('mamad_city_manual', city); // זכור שהמשתמש בחר ידנית
  updateLocationBtn();
  playSuccess();
  showToast('📍 ' + (city === 'הכל' ? 'מציג התראות לכל הארץ' : 'מציג התראות עבור: ' + city), 3000);
  // שמור עיר כ-tag ב-OneSignal לסינון פוש
  if (window.OneSignal && window.OneSignal.User && window.OneSignal.User.addTag) {
    window.OneSignal.User.addTag('city', city === 'הכל' ? 'all' : city).catch(() => {});
  }
  showScreen('screen-home');
  // בדוק מחדש אם ההתראה הפעילה רלוונטית לעיר החדשה
  checkOrefAlert();
}

function updateLocationBtn() {
  const btn = document.getElementById('locationBtn');
  if (btn) btn.textContent = selectedCity === 'הכל' ? '📍 כל הארץ — לחץ/י לשינוי' : '📍 ' + selectedCity + ' — לחץ/י לשינוי';
}

