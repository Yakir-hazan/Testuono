function add2048Tile() {
  const empty = [];
  for(let r=0;r<4;r++) for(let c=0;c<4;c++) if(!grid2048[r][c]) empty.push([r,c]);
  if(!empty.length) return;
  const [r,c] = empty[Math.floor(Math.random()*empty.length)];
  grid2048[r][c] = Math.random()<0.9?2:4;
}
function draw2048() {
  const w = canvas2048el.width;
  const ts = (w - C2.tileGap * 5) / 4;
  ctx2048.fillStyle = '#bbada0'; ctx2048.fillRect(0,0,w,w);
  for(let r=0;r<4;r++) for(let c=0;c<4;c++) {
    const v = grid2048[r][c];
    const x = C2.tileGap + c*(ts+C2.tileGap);
    const y = C2.tileGap + r*(ts+C2.tileGap);
    ctx2048.fillStyle = tileColors[v]||'#3c3a32';
    roundRect2048(ctx2048,x,y,ts,ts,8);
    if(v) {
      ctx2048.fillStyle = v<=4?'#776e65':'#f9f6f2';
      const fs = v<100?ts*0.38:v<1000?ts*0.3:ts*0.24;
      ctx2048.font = `bold ${fs}px Arial,sans-serif`;
      ctx2048.textAlign='center'; ctx2048.textBaseline='middle';
      ctx2048.fillText(v, x+ts/2, y+ts/2);
    }
  }
  document.getElementById('score2048').textContent = `ניקוד: ${score2048val}`;
}
function roundRect2048(ctx,x,y,w,h,r) { ctx.beginPath(); ctx.moveTo(x+r,y); ctx.lineTo(x+w-r,y); ctx.arcTo(x+w,y,x+w,y+r,r); ctx.lineTo(x+w,y+h-r); ctx.arcTo(x+w,y+h,x+w-r,y+h,r); ctx.lineTo(x+r,y+h); ctx.arcTo(x,y+h,x,y+h-r,r); ctx.lineTo(x,y+r); ctx.arcTo(x,y,x+r,y,r); ctx.closePath(); ctx.fill(); }
function move2048(dir) {
  let moved = false;
  const rotate = (g) => g[0].map((_,i)=>g.map(r=>r[i]).reverse());
  let g = grid2048;
  if(dir==='up') g=rotate(rotate(rotate(g)));
  else if(dir==='right') g=rotate(rotate(g));
  else if(dir==='down') g=rotate(g);
  const newG = g.map(row => {
    const tiles = row.filter(v=>v);
    const merged = [];
    for(let i=0;i<tiles.length;i++) {
      if(tiles[i+1]&&tiles[i]===tiles[i+1]) { merged.push(tiles[i]*2); score2048val+=tiles[i]*2; i++; }
      else merged.push(tiles[i]);
    }
    while(merged.length<4) merged.push(0);
    if(merged.join()!==row.join()) moved=true;
    return merged;
  });
  let result = newG;
  if(dir==='down') result=rotate(rotate(rotate(newG)));
  else if(dir==='right') result=rotate(rotate(newG));
  else if(dir==='up') result=rotate(newG);
  if(moved) { grid2048=result; add2048Tile(); draw2048(); }
}
function setupSwipe2048() {
  let sx,sy;
  const el = canvas2048el;
  el.ontouchstart = e => { sx=e.touches[0].clientX; sy=e.touches[0].clientY; };
  el.ontouchend = e => {
    const dx=e.changedTouches[0].clientX-sx, dy=e.changedTouches[0].clientY-sy;
    if(Math.abs(dx)>Math.abs(dy)) move2048(dx>0?'right':'left');
    else move2048(dy>0?'down':'up');
  };
  document.addEventListener('keydown', e => {
    if(!document.getElementById('screen-2048').classList.contains('active')) return;
    if(e.key==='ArrowLeft') move2048('left');
    else if(e.key==='ArrowRight') move2048('right');
    else if(e.key==='ArrowUp') move2048('up');
    else if(e.key==='ArrowDown') move2048('down');
  });
}

