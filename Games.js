function restoreAlertFromStorage() {
  try {
    const saved = localStorage.getItem('mamad_alert');
    if (!saved) return;
    const { title, areas, timestamp, instruction, alertAreas } = JSON.parse(saved);
    const elapsed = Date.now() - timestamp;
    const tenMin = 10 * 60 * 1000;
    const sevenMin = 7 * 60 * 1000;
    if (elapsed >= tenMin) { localStorage.removeItem('mamad_alert'); return; }

    // בדוק אם רלוונטי לעיר הנבחרת
    if (selectedCity !== 'הכל' && alertAreas && Array.isArray(alertAreas)) {
      const s = selectedCity.trim();
      const relevant = alertAreas.some(area => {
        const a = area.trim();
        return a === s || a.startsWith(s + ' ') || a.startsWith(s + ' - ') || a.startsWith(s + '-');
      });
      if (!relevant) { localStorage.removeItem('mamad_alert'); return; }
    }

    // שחזר התראה
    const chip = document.getElementById('alertChip');
    const inner = chip.querySelector('div');
    inner.innerHTML = `<span style="width:8px;height:8px;background:#ff6b6b;border-radius:50%;display:inline-block;animation:alertBlink 1.2s ease-in-out infinite"></span>
      🚨 ${title}${areas ? ' — ' + areas : ''}`;
    chip.style.display = 'flex';

    const instrEl = document.getElementById('alertInstruction');
    const instrLabel = document.getElementById('alertInstructionLabel');
    instrEl.style.display = 'flex';

    const remaining7 = sevenMin - elapsed;
    if (remaining7 <= 0) {
      instrLabel.textContent = '🛡️ הישארו במרחב המוגן עד לקבלת הודעה מפיקוד העורף';
    } else {
      instrLabel.textContent = '🏃 היכנסו למרחב המוגן עכשיו!';
      alertInstructionTimeout = setTimeout(() => {
        instrLabel.textContent = '🛡️ הישארו במרחב המוגן עד לקבלת הודעה מפיקוד העורף';
      }, remaining7);
    }

    const remainingHide = tenMin - elapsed;
    alertHideTimeout = setTimeout(() => {
      hideAlert(false);
      localStorage.removeItem('mamad_alert');
    }, remainingHide);
  } catch(e) {}
}

